using System;
using System.Collections.Generic;
using System.Dynamic;
using System.Reflection;
using Newtonsoft.Json;
namespace HMS.SP
{
    public class __deferred
    {
        [JsonProperty("uri")]
        public string uri;
        public __deferred(string json)
        {
            if (json == String.Empty)
                return;
            dynamic jobject = Newtonsoft.Json.JsonConvert.DeserializeObject(json);
            var refObj = jobject;

            if (jobject.d != null)
                refObj = jobject.d;
            if (jobject.__deferred != null)
                refObj = jobject.__deferred;

            this.uri = refObj.uri.Value;

        }
    }
}
